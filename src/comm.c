uint16 CRC(uint8 *pData, uint16 len)
{
	uint8  uchCRCHi = 0xff;
 	uint8  uchCRCLo = 0xff;
 	uint16 uIndex;
 	
 	while(len--)
 	{
 		uIndex = uchCRCHi ^ *pData++;
 		uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex];
		uchCRCLo = auchCRCLo[uIndex];
 	}
 	return ((uchCRCHi*0x100)|uchCRCLo);
}

bit CheckReciveBuff(void)
{
	uint8 Len;
	uint16 CheckOut;

   	Len = CommReciveBuff[2] - 2;

	CheckOut = CRC(CommReciveBuff, Len);

	if ((CheckOut & 0xff) == CommReciveBuff[Len + 1] &&\
		(CheckOut >> 8) == CommReciveBuff[Len])
	{
		if ((CommReciveBuff[4] == (SysParatmeter.SysUID & 0xff)) &&\
			(CommReciveBuff[3] == (SysParatmeter.SysUID >> 8)))
		{
			return 1;	
		}
	}
	
	return 0;	
}

void CommReturn(uint8 CMD)
{
	uint8 i;
	uint16 CRCData;

	CommSendBuff[0] = COMMHEAD1;
	CommSendBuff[1] = COMMHEAD2;
	for (i = 0; i < 4; i++)
	{
		CommSendBuff[3+i] = CommReciveBuff[3+i];
	}

	switch (CMD)
	{
		case SETSYSPARA :
			CommSendBuff[2] = SETPARALEN;

		case SETTIME :
			CommSendBuff[2] = SETTIMEFRAMELEN;
			
		break;

		case READTIME :
			CommSendBuff[2] = READTIMEFRAMELEN;
	
		break;

		case READTH :  //读取温湿度
			CommSendBuff[2] = THFRAMELEN;

		break;

		case IRDASEND :
			CommSendBuff[2] = IADRSENDLEN;
		break;

		default :
		break;
	}
	CRCData = CRC(CommSendBuff, CommSendBuff[2] - 2);
	CommSendBuff[CommSendBuff[2]-2] = CRCData >> 8;
	CommSendBuff[CommSendBuff[2]-1] = CRCData & 0xff;

	#if !USER485
	Puts(CommSendBuff, CommSendBuff[2]);
	#else
	Puts485(CommSendBuff, CommSendBuff[2]);
	#endif
}

void CommCmd(void)
{	
	uint8 CMD;
	uint8 i, sum = 0;
		 
	if (CommReciveSuccess)
	{
		if (CheckReciveBuff())
		{	
			CMD = CommReciveBuff[5];
			switch (CMD)
			{
				case SETSYSPARA :	 //设置参数
					if (SetSysParameter(&CommReciveBuff[7]) == TRUE)
					{
						CommSendBuff[7] = CMDSUCCESS;	
					}
					else
					{
						CommSendBuff[7] = CMDFAILED;	
					}
					CommReturn(SETTIME);
				break;

				case SETTIME :  //设置时间
					ltobcds(&CommReciveBuff[7], TIMELEN);
					DS1302_SetTime(&CommReciveBuff[7]);
					CommSendBuff[7] = CMDSUCCESS;
					CommReturn(SETSYSPARA);
				break;

				case READTIME :  //读取时间
					DS1302_GetTime(&CommSendBuff[8]);
					bcdtols(&CommSendBuff[8], TIMELEN);
					CommSendBuff[7] = CMDSUCCESS; 
					CommReturn(READTIME);
				break;

				case READTH :  //读取温湿度
					DHT11Read((DHTst_Typ *)&CommSendBuff[8]);
					for (i = 0; i < THDATALEN - 1; i++)
					{
						sum += CommSendBuff[8 + i];
					}

					if (sum == CommSendBuff[8 + i])
					{
						CommSendBuff[7] = CMDSUCCESS;	
					}
					else
					{
						CommSendBuff[7] = CMDFAILED;
					}
					CommReturn(READTH);
				break;

				case IRDASEND :
					IrDeviceSend(&CommReciveBuff[7]);
					CommSendBuff[7] = CMDSUCCESS;
					CommReturn(READTH);
				break;

				default :
				break;
			}
		}

		CommReciveSuccess = 0;
		ES = 1;
	}
}



